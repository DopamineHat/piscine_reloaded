int		ft_iterative_factorial(int nb)
{
	int x;

	if (nb > 12)
		return (0);
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	x = 1;
	while (nb > 1)
	{
		x = x * nb;
		--nb;
	}
	return (x);
}
